package classSchedule.model;

public class Description {
	private int id;
	private String description;
	
	
	public Description()
	{
		
	}
	
	public int getId()
	{
		return id;
	}
	
	public void setId(int id)
	{
		this.id = id;
	}
	
	public String getDescript()
	{
		return description;
	}
	
	public void setDescript(String desc)
	{
		this.description = desc;
	}
}
